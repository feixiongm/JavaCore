package MaxValue;

public abstract class RecArea {
    private int a;
    private int b;

    public int rea(int a, int b) {
        return a * b;
    }

    public void setA(int a) {
        this.a = a;
    }
    public int getA(int a){
        return a;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getB(int b){
         return b;
    }

}
